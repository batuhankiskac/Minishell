#include "minishell.h"

int	builtin_pwd(void)
{
	char	cwd[PATH_MAX];
	if (!getcwd(cwd, sizeof(cwd)))
	{
		perror("pwd error");
		return (ERROR);
	}
	printf("%s\n", cwd);
	return (0);
}

